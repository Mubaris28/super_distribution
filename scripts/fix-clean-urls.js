#!/usr/bin/env node
/**
 * fix-clean-urls.js – Super Distribution
 * Generates redirect/rewrite configs so clean URLs (/about, /contact, etc.) work
 * on any host (Apache, Netlify, Vercel, IIS, Cloudflare Pages).
 * Run from project root: node scripts/fix-clean-urls.js
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const REQUIRED_HTACCESS_MARKS = [
  'RewriteEngine On',
  'REQUEST_FILENAME}.html -f',
  '$1.html [L]',
];

function findHtmlFiles(dir, base = '', list = []) {
  const full = path.join(dir, base);
  if (!fs.existsSync(full) || !fs.statSync(full).isDirectory()) return list;
  const entries = fs.readdirSync(full, { withFileTypes: true });
  for (const e of entries) {
    const rel = base ? `${base}/${e.name}` : e.name;
    if (e.isDirectory()) {
      findHtmlFiles(dir, rel, list);
    } else if (e.isFile() && e.name.endsWith('.html')) {
      list.push(rel.replace(/\\/g, '/'));
    }
  }
  return list;
}

function htmlToCleanPath(relPath) {
  const withoutExt = relPath.replace(/\.html$/i, '');
  if (withoutExt === 'index') return '/';
  return '/' + withoutExt;
}

function run() {
  console.log('Super Distribution – fixing clean URLs\n');

  const htmlFiles = findHtmlFiles(ROOT);
  const skip = new Set(['index.html', '404.html']);
  const rewrites = [];

  for (const f of htmlFiles) {
    if (skip.has(path.basename(f))) continue;
    const clean = htmlToCleanPath(f);
    if (clean === '/') continue;
    rewrites.push({ clean, file: '/' + f });
  }

  rewrites.sort((a, b) => a.clean.localeCompare(b.clean));

  // 1. Netlify & Cloudflare Pages: _redirects
  const redirectsLines = rewrites.map((r) => `${r.clean} ${r.file} 200`);
  const redirectsContent = '# Clean URLs → serve .html (generated by scripts/fix-clean-urls.js)\n' + redirectsLines.join('\n') + '\n';
  const redirectsPath = path.join(ROOT, '_redirects');
  fs.writeFileSync(redirectsPath, redirectsContent, 'utf8');
  console.log('Written: _redirects (Netlify / Cloudflare Pages)');

  // 2. Vercel: vercel.json
  const vercel = {
    rewrites: rewrites.map((r) => ({ source: r.clean, destination: r.file })),
  };
  const vercelPath = path.join(ROOT, 'vercel.json');
  fs.writeFileSync(vercelPath, JSON.stringify(vercel, null, 2) + '\n', 'utf8');
  console.log('Written: vercel.json (Vercel)');

  // 3. IIS: web.config (URL rewrite)
  const escapeIisPattern = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const iisRules = rewrites
    .map((r) => {
      const pathNoSlash = r.clean.slice(1); // avoid /^\// in template
      return `    <rule name="Clean-${pathNoSlash.replace(/\//g, '-')}" stopProcessing="true">
      <match url="^${escapeIisPattern(pathNoSlash)}$" />
      <action type="Rewrite" url="${r.file.replace(/^\//, '')}" />
    </rule>`;
    })
    .join('\n');
  const webConfig = `<?xml version="1.0" encoding="UTF-8"?>
<!-- Generated by scripts/fix-clean-urls.js - IIS URL Rewrite -->
<configuration>
  <system.webServer>
    <rewrite>
      <rules>
${iisRules}
        <rule name="404" stopProcessing="true">
          <match url=".*" />
          <conditions logicalGrouping="MatchAll">
            <add input="{REQUEST_FILENAME}" matchType="IsFile" negate="true" />
            <add input="{REQUEST_FILENAME}" matchType="IsDirectory" negate="true" />
          </conditions>
          <action type="Rewrite" url="404.html" />
        </rule>
      </rules>
    </rewrite>
  </system.webServer>
</configuration>
`;
  const webConfigPath = path.join(ROOT, 'web.config');
  fs.writeFileSync(webConfigPath, webConfig, 'utf8');
  console.log('Written: web.config (IIS)');

  // 4. Verify .htaccess
  const htaccessPath = path.join(ROOT, '.htaccess');
  let htaccessOk = true;
  if (!fs.existsSync(htaccessPath)) {
    console.log('\nWarning: .htaccess not found. Create it for Apache/Hostinger.');
    htaccessOk = false;
  } else {
    const htaccess = fs.readFileSync(htaccessPath, 'utf8');
    for (const mark of REQUIRED_HTACCESS_MARKS) {
      if (!htaccess.includes(mark)) {
        console.log('\nWarning: .htaccess may be incomplete (missing: ' + mark + ')');
        htaccessOk = false;
        break;
      }
    }
    if (htaccessOk) console.log('\nChecked: .htaccess has required rewrite rules.');
  }

  // 5. Deploy checklist
  const checklistPath = path.join(ROOT, 'DEPLOY-CHECKLIST.md');
  const checklist = `# Deploy checklist – clean URLs

Generated by \`node scripts/fix-clean-urls.js\`.

## Fix 500 on /about (and other clean URLs)

- **Hostinger (Apache):**
  1. Upload \`.htaccess\` to your **document root** (e.g. \`public_html\`) – same folder as \`index.html\`.
  2. In Hostinger (or via support), ensure **mod_rewrite** is enabled for this domain.
  3. No other files needed; \`.htaccess\` handles /about → about.html.

- **Netlify / Cloudflare Pages:**
  1. Deploy the project. The \`_redirects\` file in the repo root is used automatically.
  2. Clean URLs like \`/about\` will serve \`about.html\` with status 200.

- **Vercel:**
  1. Deploy the project. \`vercel.json\` in the repo root is used automatically.
  2. Clean URLs will work via rewrites.

- **IIS (Windows server):**
  1. Upload \`web.config\` to the site root.
  2. Install the "URL Rewrite" IIS module if not already installed.

- **Behind Cloudflare (in front of Hostinger):**
  1. Ensure \`.htaccess\` is on the origin and mod_rewrite is on.
  2. Or add a Redirect Rule: URL \`superdistribution.mu/about\` → Forward to \`https://superdistribution.mu/about.html\` (302 or 301). That avoids the 500 at the edge.
`;

  fs.writeFileSync(checklistPath, checklist, 'utf8');
  console.log('Written: DEPLOY-CHECKLIST.md');

  console.log('\nSummary:');
  console.log('  - ' + rewrites.length + ' clean-URL rewrites generated.');
  console.log('  - Use DEPLOY-CHECKLIST.md for host-specific steps.');
  if (!htaccessOk) console.log('  - Fix .htaccess or use _redirects/vercel.json/web.config for your host.');
}

run();
